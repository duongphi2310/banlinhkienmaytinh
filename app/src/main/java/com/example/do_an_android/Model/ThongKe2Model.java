package com.example.do_an_android.Model;

public class ThongKe2Model {
    private String tenllinhkien;
    private String tongtienban;

    public ThongKe2Model(String tenllinhkien, String tongtienban) {
        this.tenllinhkien = tenllinhkien;
        this.tongtienban = tongtienban;
    }

    public String getTenllinhkien() {
        return tenllinhkien;
    }

    public String getTongtienban() {
        return tongtienban;
    }
}
